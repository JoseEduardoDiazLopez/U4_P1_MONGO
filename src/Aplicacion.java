    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if(!estaSeleccionado()){
            mensaje("ERROR\nDebes seleccionar una fila de la tabla para poder borrar");
        }
        int fila = jTable1.getSelectedRow();
        idGlobal = m.getValueAt(fila, 0).toString();
        int resp = JOptionPane.showConfirmDialog(this,"Â¿Estas segura que deseas borrar ID"+idGlobal+"?");
        if(resp== JOptionPane.OK_OPTION){
            if(eliminar(idGlobal)==true){
                mensaje("Se elimino correctamente");
                cargarDatos();
            }else{
                mensaje("No se pudo eliminar el ID"+idGlobal);
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarActionPerformed
        if(!estaSeleccionado()){
            mensaje("ERROR\nDebes seleccionar una fila de la tabla para poder borrar");
            return;
        }
        int fila = jTable1.getSelectedRow();
        idGlobal = m.getValueAt(fila, 0).toString();
        txtDes.setText(m.getValueAt(fila, 1).toString());
        txtP.setText(m.getValueAt(fila, 2).toString());
        txtEx.setText(m.getValueAt(fila, 3).toString());
        btnInsertar.setEnabled(false);
        btnActualizar.setEnabled(true);
        
    }//GEN-LAST:event_btnCargarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        try{
            if(idGlobal.isEmpty()){
                mensaje("ID VACIO");
                return;
            }
            Document filtro  = new Document("_id", new ObjectId(idGlobal));
            Document dataActualizar = new Document();
            dataActualizar.put("Descripcion", txtDes.getText());
            dataActualizar.put("Precio", txtP.getText());
            dataActualizar.put("Existencia", Integer.parseInt(txtEx.getText()));
            
            UpdateResult resultado = coleccion.updateOne(filtro, new Document("$set",dataActualizar));
            if(resultado.getMatchedCount()==1){
                mensaje("SE ACTUALIZO LOS DATOS");
                cargarDatos();
                limpiarCampos();
            }else{
                mensaje("ERROR\nNo se pudo actualizar");
            }
        }catch(Exception e){mensaje(e.getMessage());}
        btnInsertar.setEnabled(true);
        btnActualizar.setEnabled(false);
    }//GEN-LAST:event_btnActualizarActionPerformed
    private boolean eliminar(String id){
        try{
            Document eliminar = new Document("_id",new ObjectId(id));
            DeleteResult resultado = coleccion.deleteOne(eliminar);
            if(resultado.getDeletedCount()==1){
                return true;
            }else{
                return false;
            }
        }catch(Exception e){ mensaje(e.getMessage());}
        return false;
    }
    private boolean estaSeleccionado(){
        if(jTable1.getSelectedRow()>=0){
            return true;
        }
        return false;
    }
    public void limpiarCampos(){
        txtDes.setText("");
        txtP.setText("");
        txtEx.setText("");
        
    }
    public void cargarDatos(){
        try{
            MongoCursor<Document> documentos = coleccion.find().iterator();
            if(m.getRowCount()!=0){
                int total = m.getRowCount();
                for (int i = 1; i <= total; i++) {
                    m.removeRow(0);
                }
            }
            while(documentos.hasNext()){
                String[] renglon = new String[5];
                ArrayList<Object> doc = new ArrayList(documentos.next().values());
                renglon[0] = doc.get(0).toString();
                renglon[1] = doc.get(1).toString();
                renglon[2] = doc.get(2).toString();
                renglon[3] = doc.get(3).toString();
                
                m.addRow(renglon);
            }
        }catch(Exception e){mensaje(e.getMessage());}
    }
